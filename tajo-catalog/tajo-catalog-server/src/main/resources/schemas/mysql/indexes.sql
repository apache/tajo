CREATE TABLE INDEXES (
  INDEX_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  DB_ID INT NOT NULL,
  TID INT NOT NULL,
  INDEX_NAME VARCHAR(128) NOT NULL,
  METHOD CHAR(32) NOT NULL,
  EXPR_NUM INT NOT NULL,
  EXPRS VARCHAR(1024) NOT NULL,
  EXPR_TYPES VARCHAR(1024) NOT NULL,
  ASC_ORDERS VARCHAR(1024) NOT NULL,
  NULL_ORDERS VARCHAR(1024) NOT NULL,
  IS_UNIQUE BOOLEAN NOT NULL,
  IS_CLUSTERED BOOLEAN NOT NULL,
  PRED VARCHAR(4096),
  FOREIGN KEY (DB_ID) REFERENCES DATABASES_ (DB_ID) ON DELETE CASCADE,
  FOREIGN KEY (TID) REFERENCES TABLES (TID) ON DELETE CASCADE,
  UNIQUE INDEX IDX_DB_ID_NAME (DB_ID, TID, INDEX_NAME),
)